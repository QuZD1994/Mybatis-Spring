package cn.mybatisspring.entity;

public class Users {

    private Long id;
    private String name;
    private String pwd;
    private Long roleId;
    private String roleName;
    private Role role;
    public Users() {
    }

    public Users(Long id, String name, String pwd, Long roleId) {
        this.id = id;
        this.name = name;
        this.pwd = pwd;
        this.roleId = roleId;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public Long getRoleId() {
        return roleId;
    }

    public void setRoleId(Long roleId) {
        this.roleId = roleId;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }
}
